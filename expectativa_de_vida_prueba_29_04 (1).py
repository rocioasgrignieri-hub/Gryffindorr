flag_cache = {}

def get_flag_base64(iso2):
    if pd.isna(iso2):
        return ""

    iso2 = str(iso2).strip().lower()

    if iso2 == "" or iso2 == "not found":
        return ""

    if iso2 in flag_cache:
        return flag_cache[iso2]

    urls = [
        f"https://flagcdn.com/w80/{iso2}.png",
        f"https://flagcdn.com/w160/{iso2}.png"
    ]

    headers = {
        "User-Agent": "Mozilla/5.0"
    }

    for url in urls:
        try:
            response = requests.get(url, headers=headers, timeout=15)

            if response.status_code == 200 and len(response.content) > 500:
                encoded = base64.b64encode(response.content).decode("utf-8")
                flag_source = f"data:image/png;base64,{encoded}"
                flag_cache[iso2] = flag_source
                return flag_source

        except Exception:
            pass

    fallback = f"https://flagcdn.com/w80/{iso2}.png"
    flag_cache[iso2] = fallback
    return fallback

