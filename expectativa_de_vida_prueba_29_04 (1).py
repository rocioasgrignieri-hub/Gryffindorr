    try:
        url = flag_urls[country]
        response = requests.get(url, timeout=5)
        img = Image.open(BytesIO(response.content)).convert("RGB")

        # Color dominante (promedio excluyendo píxeles muy claros/oscuros)
        arr = np.array(img).reshape(-1, 3).astype(float)
        brightness = arr.mean(axis=1)
        mask = (brightness > 30) & (brightness < 220)
        filtered = arr[mask] if mask.sum() > 0 else arr
        dominant = tuple((filtered.mean(axis=0) / 255).tolist())

        # Miniatura para poner al costado
        thumb = img.resize((60, 38), Image.LANCZOS)
        return dominant, np.array(thumb)
    except:
        return (0.6, 0.6, 0.6), None

